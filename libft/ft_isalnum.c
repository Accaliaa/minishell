int	ft_isalnum(int c)
{
	if ((c >= 65 && c <= 90) || (c >= 97 && c <= 122) || (c >= 48 && c <= 57) || (c == 32)
		|| (c == 60) || (c == 62))
		return (1);
	return (0);
}
