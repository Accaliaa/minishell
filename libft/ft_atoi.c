#include "libft.h"

int	ft_atoi(const char *str)
{
	int				n;
	unsigned long	nb;
	int				i;

	i = 0;
	n = 1;
	nb = 0;
	while (str[i] == 32 || (str[i] >= 9 && str[i] <= 13))
		i++;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			n *= -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		nb = nb * 10 + (str[i] - '0');
		i++;
	}
	if (n == 1 && nb > 9223372036854775807)
		return (-1);
	if (n == -1 && nb > 9223372036854775808u)
		return (0);
	return ((int)nb * n);
}
